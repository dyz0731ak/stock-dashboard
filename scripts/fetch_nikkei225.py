# 重複除去（同じコードは最初の出現を残す）
_seen = set()
N225_DEDUP = []
for code, name, sector in N225:
    if code in _seen:
        continue
    _seen.add(code)
    N225_DEDUP.append((code, name, sector))


def fetch_one(code, name, sector):
    """1銘柄分: 現在値・前日終値・時価総額を取得"""
    ticker = f"{code}.T"
    try:
        t = yf.Ticker(ticker)
        hist = t.history(period="5d", interval="1d", auto_adjust=False)
        # 市場クローズ直後などで最終足が NaN になることがあるため除去してから使う
        closes = hist["Close"].dropna() if not hist.empty else hist.get("Close")
        if closes is None or len(closes) < 2:
            return None
        prev_close = float(closes.iloc[-2])
        price      = float(closes.iloc[-1])
        change_pct = ((price - prev_close) / prev_close * 100) if prev_close else 0.0

        # 時価総額: fast_info.market_cap → shares×price → info.marketCap の順でフォールバック
        def _valid(x):
            try:
                x = float(x)
                return x if (x > 0 and not math.isnan(x)) else None
            except Exception:
                return None

        market_cap = None
        try:
            market_cap = _valid(t.fast_info.get("market_cap") if hasattr(t, "fast_info") else None)
        except Exception:
            pass
        if not market_cap:
            try:
                shares = _valid(t.fast_info.get("shares") if hasattr(t, "fast_info") else None)
                if shares:
                    market_cap = _valid(shares * price)
            except Exception:
                pass
        if not market_cap:
            try:
                market_cap = _valid(t.get_info().get("marketCap"))
            except Exception:
                pass

        return {
            "code": code,
            "ticker": ticker,
            "name": name,
            "sector": sector,
            "price": round(price, 2),
            "prev_close": round(prev_close, 2),
            "change_pct": round(change_pct, 3),
            "market_cap": int(market_cap) if market_cap else None,
        }
    except Exception as e:
        print(f"  [{ticker}] error: {e}", file=sys.stderr)
        return None


def main():
    print(f"[日経225ヒートマップ] {len(N225_DEDUP)}銘柄 取得開始...", file=sys.stderr)

    results = []
    with ThreadPoolExecutor(max_workers=12) as ex:
        futures = {ex.submit(fetch_one, c, n, s): (c, n, s) for c, n, s in N225_DEDUP}
        for fut in as_completed(futures):
            r = fut.result()
            if r:
                results.append(r)

    # 市場キャップ降順でソート（後段で扱いやすく）
    results.sort(key=lambda x: -(x.get("market_cap") or 0))

    out = {
        "items": results,
        "updated_at": datetime.datetime.now(JST).isoformat(),
    }

    saved = safe_save(
        "data/nikkei225.json",
        out,
        lambda d: len(d.get("items", [])),
        label="日経225ヒートマップ",
    )

    print(json.dumps({
        "status": "ok" if saved else "kept_existing",
        "count": len(results),
    }))


if __name__ == "__main__":
    main()
